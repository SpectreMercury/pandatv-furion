def loadImage(url):
    img = cv2.cv2.imread(url)
    return img


def predict(path):
    dir_path = path
    try:
        image = Image.open(dir_path)
        height = image.height
        width = image.width
        port_area = (width * 0.25, height * 0.85, width * 0.40, height * 0.99)
        image = image.crop(port_area)
        image.save(dir_path)
        img = loadImage(dir_path)
        hero = match(img)
        # Initiate SIFA detector
        return hero
    except:
        return -1, 'notpic', '0', '0', '0'


def match(img):
    hsv = cv2.cvtColor(img, cv2.COLOR_BGR2HSV)
    lower_yellow = np.array([14, 34, 34])
    upper_yellow = np.array([32, 220, 220])
    mask = cv2.inRange(hsv, lower_yellow, upper_yellow)
    cimg = cv2.medianBlur(mask, 5)

    circles = cv2.HoughCircles(cimg, cv2.HOUGH_GRADIENT, 1, 100, param1=50, param2=20, minRadius=32, maxRadius=0)
    if circles is not None:
        big_circle = np.uint16(np.around(circles))[0][0]
        x_min = max(0, int(big_circle[0]) - int(big_circle[2]))
        x_max = min(big_circle[0] + big_circle[2], img.shape[1] - 1)
        y_min = max(0, int(big_circle[1]) - int(big_circle[2]))
        y_max = min(big_circle[1] + big_circle[2], img.shape[0] - 1)

        img = cv2.cvtColor(img, cv2.COLOR_BGR2GRAY)
        img = cv2.resize(img[y_min:y_max, x_min:x_max], (120, 120))
        return detect(img)
    else:
        return 0, 'unknown', '0', '0', '0'


