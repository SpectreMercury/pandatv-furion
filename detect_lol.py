def format_response(max, hero, ori, tar, good):
    global hero_map
    result = {}
    if max < 0:
        result["errno"] = 401
        result["errmsg"] = "cannot read pic"
        result["data"] = {}
    elif max < 1:
        result["errno"] = 402
        result["errmsg"] = "detect failed"
        result["data"] = {}
    else:
        result["errno"] = 0
        result["errmsg"] = "success"
        result["data"] = {
            "heroKey": hero,
            "role": getRole(hero_map[hero]),
            "cnName": hero_map[hero],
            "ori": ori,
            "tar": tar[0],
            "good": good
        }
    return result

