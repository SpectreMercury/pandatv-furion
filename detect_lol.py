def saveImage(url):
    if not os.path.exists(os.getcwd() + '/lol_hero/'):
        os.mkdir(os.getcwd() + '/lol_hero/')
    file_md5 = hashlib.md5(url).hexdigest()
    file_path = os.getcwd() + '/lol_hero/' + file_md5 + '.jpg'
    urllib.urlretrieve(url, file_path)
    return file_path, file_md5


